fun main() {

    // Semicolons are optional
    // Print line
    println("Hello World")

    // Immutable variable. Constant. Type inference
    val myName = "Sheraz"

    // Mutable variable. Type inference
    var myAge = 40

    println("My name is " + myName + " I am " + myAge + " years old.")

    // Defining variable type. Types start with Caps
    val mySalary: Double;
    mySalary = 100.22;

    // String interpolation
    println("My salary is ${mySalary}")
}
